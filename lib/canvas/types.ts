// Canvas Type Definitions for Spatial AI Canvas System
// Inspired by Eden.so - Surpassing Notion's limitations

import { Node, Edge, XYPosition } from 'reactflow';

// ============================================================================
// CORE CANVAS TYPES
// ============================================================================

export type NodeType = 
  | 'image' 
  | 'video' 
  | 'document' 
  | 'note' 
  | 'postit'
  | 'ai-chat' 
  | 'ai-generator' 
  | 'group'
  | 'audio'
  | 'url'
  | 'social-post'
  | 'shape';

export type ConnectionType = 
  | 'reference'      // Used as reference/inspiration
  | 'ai-input'       // Input to AI
  | 'ai-output'      // Generated by AI
  | 'inspiration'    // Loose inspiration
  | 'style-source';  // Style reference

export interface BaseNodeData {
  label: string;
  createdAt: Date;
  updatedAt: Date;
  userId: string;
  metadata?: Record<string, unknown>;
  tags?: string[];
  aiTags?: string[];
  isLocked?: boolean;
  description?: string;
}

// ============================================================================
// SPECIFIC NODE DATA TYPES
// ============================================================================

export interface ImageNodeData extends BaseNodeData {
  type: 'image';
  url: string;
  thumbnail?: string;
  width: number;
  height: number;
  fileSize: number;
  mimeType: string;
  mediaLibraryId?: string;
  aiAnalysis?: {
    objects: string[];
    colors: string[];
    style: string[];
    composition: string;
    mood: string;
  };
  exif?: Record<string, unknown>;
}

export interface VideoNodeData extends BaseNodeData {
  type: 'video';
  url: string;
  thumbnail?: string;
  duration: number;
  width: number;
  height: number;
  fileSize: number;
  transcription?: {
    text: string;
    timestamps: Array<{ time: number; text: string }>;
  };
  scenes?: Array<{
    timestamp: number;
    thumbnail: string;
    description: string;
  }>;
  mediaLibraryId?: string;
}

export interface DocumentNodeData extends BaseNodeData {
  type: 'document';
  url: string;
  fileType: 'pdf' | 'docx' | 'txt' | 'md';
  pageCount?: number;
  extractedText?: string;
  summary?: string;
  mediaLibraryId?: string;
}

export interface NoteNodeData extends BaseNodeData {
  type: 'note';
  content: string; // TipTap JSON or HTML
  backgroundColor?: string;
  textColor?: string;
  fontSize?: 'small' | 'medium' | 'large';
  isPinned?: boolean;
  databaseNoteId?: string; // ID of the synced note in the database
}

export interface PostItCheckItem {
  id: string;
  text: string;
  checked: boolean;
  isCheckbox?: boolean; // false = plain text, true = checkbox item
  highlighted?: boolean;
  highlightColor?: string;
}

export interface PostItNodeData extends BaseNodeData {
  type: 'postit';
  items: PostItCheckItem[];
  backgroundColor?: 'green' | 'yellow' | 'pink' | 'blue' | 'orange' | 'purple';
}

export interface UrlNodeData extends BaseNodeData {
  type: 'url';
  url: string;
  title?: string;
  description?: string;
  thumbnail?: string;
  favicon?: string;
  siteName?: string;
}

export interface AIChatNodeData extends BaseNodeData {
  type: 'ai-chat';
  model: AIModel;
  conversation: Message[];
  connectedAssetIds: string[];
  systemPrompt?: string;
  temperature?: number;
  maxTokens?: number;
  isExpanded: boolean;
  contextSummary?: string;
}

export interface AIGeneratorNodeData extends BaseNodeData {
  type: 'ai-generator';
  provider: AIImageProvider;
  prompt: string;
  negativePrompt?: string;
  referenceImageIds: string[];
  parameters: GenerationParameters;
  generationHistory: GeneratedImage[];
  status: 'idle' | 'generating' | 'completed' | 'error';
  error?: string;
  currentJobId?: string;
}

export interface GroupNodeData extends BaseNodeData {
  type: 'group';
  childNodeIds: string[];
  backgroundColor?: string;
  borderColor?: string;
  isCollapsed?: boolean;
}

export interface AudioNodeData extends BaseNodeData {
  type: 'audio';
  url: string;
  duration: number;
  waveform?: number[];
  transcription?: string;
  mediaLibraryId?: string;
}

export interface SocialPostNodeData extends BaseNodeData {
  type: 'social-post';
  url: string;
  platform?: string;
  postId?: string;
  post?: unknown; // SocialPost type from social/types
  status: 'idle' | 'loading' | 'loaded' | 'error';
  error?: {
    type: string;
    message: string;
    platform: string;
  };
  displayMode: 'compact' | 'full' | 'expanded';
  theme: 'light' | 'dark' | 'auto';
  showMetrics: boolean;
  lastRefreshed?: Date;
}

export type ShapeType = 'rectangle' | 'circle' | 'triangle';

export type ShapeBackgroundColor = 
  | 'transparent'
  | '#ffffff'  // white
  | '#e5e7eb'  // gray
  | '#fef3c7'  // yellow
  | '#fed7aa'  // orange
  | '#fecaca'  // red/pink
  | '#d9f99d'  // lime
  | '#bbf7d0'  // green
  | '#a5f3fc'  // cyan
  | '#bfdbfe'  // blue
  | '#ddd6fe'  // purple
  | '#fbcfe8'  // pink
  | '#1f2937'; // dark

export interface ShapeNodeData extends BaseNodeData {
  type: 'shape';
  shapeType: ShapeType;
  width: number;
  height: number;
  backgroundColor: ShapeBackgroundColor;
  hasShadow: boolean;
  hasRoundedCorners: boolean;
  borderColor?: string;
  borderWidth?: number;
}

// Union type for all node data types
export type CanvasNodeData = 
  | ImageNodeData 
  | VideoNodeData 
  | DocumentNodeData 
  | NoteNodeData 
  | PostItNodeData
  | UrlNodeData
  | AIChatNodeData 
  | AIGeneratorNodeData 
  | GroupNodeData
  | AudioNodeData
  | SocialPostNodeData
  | ShapeNodeData;

