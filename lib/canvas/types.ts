export type AIModel = 
  | 'claude-sonnet-4' 
  | 'claude-opus-4'
  | 'gpt-4-turbo' 
  | 'gpt-4' 
  | 'gpt-4o'
  | 'gemini-pro'
  | 'gemini-3-flash-preview';

export type AIImageProvider = 
  | 'dalle-3' 
  | 'midjourney' 
  | 'stable-diffusion-xl'
  | 'flux-pro'
  | 'flux-dev'
  | 'imagen';

export interface Message {
  id: string;
  role: 'user' | 'assistant' | 'system';
  content: string;
  timestamp: Date;
  attachments?: Array<{
    type: 'image' | 'document';
    nodeId: string;
    url: string;
  }>;
}

export interface GenerationParameters {
  aspectRatio: '1:1' | '16:9' | '4:3' | '9:16' | '3:2' | '2:3';
  quality: 'standard' | 'hd' | 'ultra';
  style?: string;
  numberOfImages?: number;
  seed?: number;
  guidance?: number;
  steps?: number;
}

export interface GeneratedImage {
  id: string;
  url: string;
  thumbnail?: string;
  prompt: string;
  revisedPrompt?: string;
  provider: AIImageProvider;
  parameters: GenerationParameters;
  timestamp: Date;
  width: number;
  height: number;
  seed?: number;
  inCanvas: boolean;
  savedToLibrary: boolean;
  mediaLibraryId?: string;
}

// ============================================================================
// CONNECTION/EDGE TYPES
// ============================================================================

export interface CanvasConnectionData {
  label?: string;
  color?: string;
  style?: 'solid' | 'dashed' | 'dotted';
  animated?: boolean;
  strength?: number;
  connectionType?: ConnectionType;
  metadata?: Record<string, unknown>;
}

export type CanvasConnection = Edge<CanvasConnectionData>;

// ============================================================================
// CANVAS NODE (React Flow Node wrapper)
// ============================================================================

export type CanvasNode = Node<CanvasNodeData>;

// ============================================================================
// CANVAS STATE
// ============================================================================

export interface CanvasState {
  workspaceId: string;
  canvasId: string;
  nodes: CanvasNode[];
  edges: CanvasConnection[];
  viewport: {
    x: number;
    y: number;
    zoom: number;
  };
  selectedNodeIds: string[];
  copiedNodes?: CanvasNode[];
  isLoading: boolean;
  isSaving: boolean;
  lastSaved?: Date;
  collaborators: Collaborator[];
  version: number;
  metadata: CanvasMetadata;
}

export interface CanvasMetadata {
  name: string;
  description?: string;
  thumbnail?: string;
  tags: string[];
  category?: string;
  isPublic: boolean;
}

// ============================================================================
// COLLABORATION TYPES
// ============================================================================

export interface Collaborator {
  userId: string;
  userName: string;
  userAvatar?: string;
  color: string;
  cursor?: XYPosition;
  selectedNodeIds: string[];
  isActive: boolean;
  lastSeen: Date;
}

export interface CollaborationEvent {
  type: 'cursor-move' | 'node-update' | 'node-add' | 'node-delete' | 'edge-add' | 'edge-delete' | 'selection-change';
  userId: string;
  timestamp: Date;
  data: unknown;
}

